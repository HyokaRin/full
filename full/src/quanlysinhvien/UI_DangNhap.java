    private void jButton1_UI_DangNhap_LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1_UI_DangNhap_LoginActionPerformed
        String username = jTextField1_UI_DangNhap_Usrname.getText();
        String password = new String(jPasswordField1_UI_DangNhap_Pwd.getPassword());

        try {
            outputStream.writeObject(username);
            outputStream.writeObject(password);

            ObjectInputStream inputStream = new ObjectInputStream(socket.getInputStream());
            boolean isAuthenticated = (boolean) inputStream.readObject();

            if (isAuthenticated) {
                JOptionPane.showMessageDialog(this, "Login successful!");
                new giaoDienGV().setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Tài khoản hoặc mật khẩu không hợp lệ.", "Error!", JOptionPane.ERROR_MESSAGE);
            }
        } catch (IOException | ClassNotFoundException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi kết nối.", "Error!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1_UI_DangNhap_LoginActionPerformed

