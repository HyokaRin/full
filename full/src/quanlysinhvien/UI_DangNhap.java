    private void jPasswordField1_UI_DangNhap_PwdFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jPasswordField1_UI_DangNhap_PwdFocusGained
        jCheckBox1_UI_DangNhap_show.setSelected(false);
        jPasswordField1_UI_DangNhap_Pwd.setEchoChar('•');
        String pwd = String.valueOf(jPasswordField1_UI_DangNhap_Pwd.getPassword());
        if (pwd.toLowerCase().equals("password")) {
            jPasswordField1_UI_DangNhap_Pwd.setText("");
            jPasswordField1_UI_DangNhap_Pwd.setForeground(Color.black);
        }
    }//GEN-LAST:event_jPasswordField1_UI_DangNhap_PwdFocusGained

    private void jPasswordField1_UI_DangNhap_PwdFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jPasswordField1_UI_DangNhap_PwdFocusLost
        String password = String.valueOf(jPasswordField1_UI_DangNhap_Pwd.getPassword());
        if (password.toLowerCase().equals("password") || password.toLowerCase().equals("")) {
            jPasswordField1_UI_DangNhap_Pwd.setText("Password");
            jPasswordField1_UI_DangNhap_Pwd.setEchoChar((char) 0);
            jPasswordField1_UI_DangNhap_Pwd.setForeground(Color.gray);
        }
    }//GEN-LAST:event_jPasswordField1_UI_DangNhap_PwdFocusLost

